public class Test01
{


	public static void main(String[] args){
		boolean booleanVar = true;//定义布尔类型
		byte b=127;//定义byte
		short s = 129;
		int i = 310000;
		long l = 1000L;//可以l或者L
		float f = 88.99F;//小写f或者F 不可以省略
		double d = 888888.99;//可以加上d或者D
		
		System.out.println("booleanVar="+booleanVar);//booleanVar=true
		System.out.println("b="+b);//b=127
		System.out.println("s="+s);
		System.out.println("i="+i);
		System.out.println("l="+l);
		System.out.println("f="+f);
		System.out.println("d="+d);



	
	
	}
}